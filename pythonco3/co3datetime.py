import datetime
t=datetime.time(22,56,44)
print(t)
print("hour",t.hour)
print("min",t.minute)
print("se",t.second)
print("mis",t.microsecond)
print("***********************")
d=datetime.date.today()
print(d)
print("year",d.year)
print("month",d.month)
print("day",d.day)
print(":::::")
d1=datetime.date.today()
print(d1)
td=datetime.timedelta(days=2)
print(td)
d2=d1+td
print(d2)
print("====")
dt=datetime.datetime.combine(d,t)
print(dt)
